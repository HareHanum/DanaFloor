// Minimal HTML escaper for safely interpolating untrusted strings into email
// templates and other HTML. Not a sanitizer — strips nothing, just escapes.
export function escapeHtml(value: unknown): string {
  if (value === null || value === undefined) return "";
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
